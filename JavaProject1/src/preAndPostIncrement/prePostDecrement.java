package preAndPostIncrement;

public class prePostDecrement {

	public static void main(String[] args) {

	
	
	int i=2;
	int k=--i;
	int m=i--;
	
	System.out.println(k); // print k as 1
	System.out.println(m); // print m as 1
	
	System.out.println(i); // i is 0 after --i and i--
	
	
	
	
	
	
	
	
	
	
	
	
	
	}

}
